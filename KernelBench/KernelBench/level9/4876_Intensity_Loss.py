import torch
import torch.nn as nn
import torch.nn.functional
import torch.nn


class Intensity_Loss(nn.Module):

    def __init__(self):
        super().__init__()

    def forward(self, gen_frames, gt_frames):
        return torch.mean(torch.abs((gen_frames - gt_frames) ** 2))


