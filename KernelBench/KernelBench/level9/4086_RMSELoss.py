import torch
from torch import nn
import torch.cuda


class RMSELoss(nn.Module):

    def __init__(self, eps=1e-06):
        super().__init__()
        self.mse = nn.MSELoss()
        self.eps = eps

    def forward(self, yhat, y):
        loss = torch.sqrt(self.mse(yhat, y) + self.eps)
        return loss


